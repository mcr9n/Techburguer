	private Cliente encontrarCliente(String cpf, ArrayList<Cliente> clientes) {
		for (Cliente cliente : clientes) {
			if (cliente.getCpf() == cpf) {
				return cliente;
			}
		}
		return null;
	}

	private boolean clienteExiste(String cpf, ArrayList<Cliente> clientes) {
		for (Cliente cliente : clientes) {
			if (cliente.getCpf() == cpf) {
				return true;
			}
		}
		return false;
	}

