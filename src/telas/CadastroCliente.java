    private void btnEntrarCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarCadastrarActionPerformed
		String cpf = this.cpfTextField.getText();
		String name = this.nameTextField.getText();
		String dataDeNascimento = this.dataDeNascimentoTextField.getText();

		if (Validacoes.isEmpty(name) || Validacoes.isEmpty(cpf) || Validacoes.isEmpty(dataDeNascimento)) {
			JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos.", "Erro", JOptionPane.PLAIN_MESSAGE);
			return;
		}

		if (!Validacoes.isCPF(cpf)) {
			JOptionPane.showMessageDialog(null, "Por favor, digite o CPF no seguinte formato: 000.000.000-00.", "Erro", JOptionPane.PLAIN_MESSAGE);
			return;
		}

		if (!Validacoes.isData(dataDeNascimento)) {
			JOptionPane.showMessageDialog(null, "Por favor, digite uma data valida.", "Erro", JOptionPane.PLAIN_MESSAGE);
			return;
		}

		if (clienteExiste(cpf, clientes)) {
			clienteAtual = encontrarCliente(cpf, clientes);
		} else {
			clienteAtual = new Cliente(cpf, name, dataDeNascimento);
			clientes.add(clienteAtual);
		}

		// Proxima pagina
		FazerPedido telaFazerPedido = new FazerPedido();
		telaFazerPedido.setVisible(true);
		this.setVisible(false);
    }//GEN-LAST:event_btnEntrarCadastrarActionPerformed

